showit = (pass) =>{
    main = document.querySelector(".photo")
    main.innerText = null
    pass.forEach(ele => {
        div = document.createElement("div")
        div.className = "divname"
        i = document.createElement("img")
        i.src = ele.img
        pink = document.createElement("p")
        pink.innerText = ele.category
        pink.className = "pink"

        black = document.createElement("p")
        black.innerText = ele.title
        black.className = "black"
        gray = document.createElement("p")
        gray.innerText = ele.detail
        gray.className = "gray"
        read = document.createElement("a")
        read.innerText = "Read more"
        read.className = "read"
        div.append(i,pink,black,gray,read)
        main.append(div)
    });
}

showit(category)


change = () =>{
    dt = document.querySelector("#cat_list").value
    if(dt == "Category")
        showit(category)

    if(dt == "Skin")
        showit(Skin)

    if(dt == "Make Up")
        showit(Make_Up)
}


select = document.querySelector("#cat_list")
select.addEventListener("click",change)


dd = document.querySelectorAll(".divname")
dd.forEach(ele => {
    ele.addEventListener("click",clicked)
});

function clicked(ele){
    parent = ele.target.parentElement
    child = parent.children[1].innerText
    if(child == "Nail Art")
       window.location.href = "nailArt.html" 
      

    if(child == "Hair Styles")
       window.location.href = "hairStyle.html" 

    if(child == "Face Makeup")
       window.location.href = "faceMakeup.html" 
}
